package org.techfire.team225.robot;
/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class PortMap {
	
    public static int LEFT_FORWARD_MOTOR = 3;
    public static int LEFT_BACK_MOTOR = 1;
    public static int RIGHT_FORWARD_MOTOR = 4;
    public static int RIGHT_BACK_MOTOR = 2;
    public static int FRONT_MOTOR = 5;
    public static int BACK_MOTOR = 0;
}
